package com.lnt.txttopdf;

public class widthIdentifier {

    public static void main(String[] args) {
        String lines = "-\n" +
                "-------------\n" +
                "---------------------------\n" +
                "-----------------------------------------\n" +
                "-------------------------------------------------------\n" +
                "---------------------------------------------------------------------\n" +
                "-----------------------------------------------------------------------------------\n" +
                "-------------------------------------------------------------------------------------------------\n" +
                "---------------------------------------------------------------------------------------------------------------\n" +
                "----------------------------------------------------------------------------------------------------------------------------\n" +
                "------------------------------------------------------------------------------------------------------------------------------------------\n" +
                "--------------------------------------------------------------------------------------------------------------------------------------------------------\n" +
                "----------------------------------------------------------------------------------------------------------------------------------------------------------------------\n" +
                "------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------";

        int i = 0;
        for (String line : lines.split("\n")) {
            System.out.println(i + "=" + line.length());
            i+=100;
        }

    }
}
